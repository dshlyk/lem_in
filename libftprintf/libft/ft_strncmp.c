#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t len)
{
	int	x;

	x = 0;
	while ((s1[x] || s2[x]) && len-- > 0)
	{
		if (s1[x] != s2[x])
			return ((unsigned char)s1[x] - (unsigned char)s2[x]);
		x++;
	}
	return (0);
}
